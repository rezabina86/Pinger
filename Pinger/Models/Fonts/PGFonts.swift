import UIKit

extension UIFont {
    
    struct PGFonts {
        
        static var H1Font : UIFont? {
            return UIFont.systemFont(ofSize: 16, weight: .semibold)
        }
        
        static var CaptionFont : UIFont? {
            return UIFont.systemFont(ofSize: 14, weight: .regular)
        }

    }
        
}
